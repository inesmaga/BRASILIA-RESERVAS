    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:

        if (txtmaterial.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Material");
            txtmaterial.requestFocus();
            return;
        }
        if (txtprecio.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Precio del Material");
            txtprecio.requestFocus();
            return;
        }
        if (txtcantidad.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una Cantidad del Material");
            txtcantidad.requestFocus();
            return;
        }
        if (txtreparacion.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un motivo de compra del Material");
            txtreparacion.requestFocus();
            return;
        }
       
        vgastos dts = new vgastos();
        fgastos func = new fgastos();
        
        dts.setIdapartamento(Integer.parseInt(txtidapartamento.getText()));
         Calendar cal, cal1, cal2;
        int d, m, a;
        cal = dcfechagasto.getCalendar();
        d = cal.get(Calendar.DAY_OF_MONTH);
        m = cal.get(Calendar.MONTH);
        a = cal.get(Calendar.YEAR) - 1900;
        Date fg = new Date(a, m, d);
        dts.setFecha_gasto(fg);
        dts.setMaterial(txtmaterial.getText());
        dts.setCantidad(Integer.parseInt(txtcantidad.getText()));
        dts.setReparacion(txtreparacion.getText());
        dts.setPrecio(Double.parseDouble(txtprecio.getText()));
        
        dts.setTotalgastado(Double.parseDouble(txtgastado.getText()));
      
        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showConfirmDialog(rootPane, "El Gasto fue registrado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }

        } else if (accion.equals("editar")) {
            dts.setIdgastos(Integer.parseInt(txtidgasto.getText()));

            if (func.editar(dts)) {
                JOptionPane.showConfirmDialog(rootPane, "El Gasto fue editado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

