    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:

        if (txtidapartamento.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes seleccionar un Apartamento");
            txtidapartamento.requestFocus();
            return;
        }
        if (txtidreserva.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes seleccionar una  Reserva");
            txtidreserva.requestFocus();
            return;
        }
        vconsumo dts = new vconsumo();
        fconsumo func = new fconsumo();
        
        dts.setIdapartamento(Integer.parseInt(txtidapartamento.getText()));
        dts.setIdreserva(Integer.parseInt(txtidreserva.getText()));
        dts.setPeriodo(txtperiodo.getText());
        dts.setPrecio(Double.parseDouble(txtprecio.getText()));
       int seleccionado = cboestado.getSelectedIndex();
        dts.setEstado((String) cboestado.getItemAt(seleccionado));

        if (accion.equals("guardar")) {
            
            if (func.insertar(dts)) {
                JOptionPane.showConfirmDialog(rootPane, "El Consumo de la Reserva  "   + txtidreserva.getText() +  "  fue registrado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }

        } else if (accion.equals("editar")) {
            dts.setIdconsumo(Integer.parseInt(txtidconsumo.getText()));
            dts.setIdreserva(Integer.parseInt(txtidreserva.getText()));

            if (func.editar(dts)) {
                JOptionPane.showConfirmDialog(rootPane, "El Consumo de la Reserva "  + txtidreserva.getText() +  "  fue modificado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

